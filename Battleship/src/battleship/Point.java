package battleship;

/**
 *
 * @author Teo-PC
 */
public class Point {
    public int X;
    public int Y;
    
    public Point(int x, int y){
        this.X = x;
        this.Y = y;
    }
   
    @Override
    public boolean equals(Object obj){
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Point other = (Point) obj;
        if (this.X != other.X) {
            return false;
        }
        if (this.Y != other.Y) {
            return false;
        }
        return true;
    }
}

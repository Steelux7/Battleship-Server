    private void CreaMatrizUsuario() {
        this.MatrizUsuario = new JButton[10][10];
       // this.MatrizCasillasUsuario = new Casilla[15][15];
        for (int fila = 0; fila < 10; fila++) {
            for (int columna = 0; columna < 10; columna++) {
         //       this.MatrizCasillasUsuario[fils][colums] = this.CasillaUsuario;
                Casilla boton = new Casilla(fila,columna);
                this.add(boton);
                boton.setBounds(20 + (fila * 30), 30 + (columna * 30), 30, 30);
                boton.setBackground(Color.DARK_GRAY);
                this.MatrizUsuario[fila][columna] = boton;
                MatrizUsuario[fila][columna].setActionCommand(fila + "," + columna);
                MatrizUsuario[fila][columna].addMouseListener(new MouseAdapter() {//aclickSobreTablero(evt);Ã±ade el listener al boton

                    @Override
                    public void mouseClicked(java.awt.event.MouseEvent evt) {
                        if (boton.getBackground()==Color.DARK_GRAY){
                            if(ColocandoBarcos){
                                System.out.println(matrix.panel.getAlineacion());
                                System.out.println(matrix.panel.getShipSize());
                                boton.setBackground(Color.red);
                                System.out.println("Fila: "+boton.getFila());
                                System.out.println("Columna: "+boton.getColumna());
                            }
                            if(TurnoJugador){
                                boton.setBackground(Color.blue);
                                System.out.println("Fila: "+boton.getFila());
                                System.out.println("Columna: "+boton.getColumna());                            
                            }
                      
                        }
                    }
                    
                });
                
            }
        }
    }

